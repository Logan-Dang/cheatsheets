# Cheatsheet 1: Data Types and Variables in Python
# This is a comment in Python. It starts with a # and continues to the end of the line.
# Everything after a # will always be ignored by Python.

# Different data types in Python
"Hello World" # String (str)
42 # Integer (int)
3.14 # Float
True # Boolean (bool)
None # NoneType (None)
[1, 2, 3] # List
(1, 2, 3) # Tuple
{1, 2, 3} # Set
{'name': 'Alice', 'age': 42} # Dictionary (dict)

# How to declare a variable in Python
name = "Alice" # In Jargonspeak, we can say "set the variable name to the value Alice"
num = 42 # Set the variable num to the value 42
l = [1, 2, 3] # Set the variable l to the list [1, 2, 3]

# How to produce output in Python
print("Hello World") # Print the string "Hello World"
print(num) # Print the value of the variable num
# Note that none of the lines above 22 produce any output as they are just variable declarations
# For our purposes, the print function is the only way to produce output in Python